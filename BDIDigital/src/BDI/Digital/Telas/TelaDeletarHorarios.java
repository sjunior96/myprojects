    public void consultaHorarios(){
        String sql =    "SELECT HORARIOS.cod_horario, HORARIOS.horario, LINHAS.cod_linha, LINHAS.prefixoLinha, LINHAS.itinerarioLinha\n" +
                        "FROM HORARIOS_POR_LINHA \n" +
                        "INNER JOIN HORARIOS ON HORARIOS.cod_horario = HORARIOS_POR_LINHA.cod_horario\n" +
                        "INNER JOIN LINHAS ON LINHAS.cod_linha = HORARIOS_POR_LINHA.cod_linha order by HORARIOS.horario;";
        ResultSet rs = null;
        PreparedStatement pst = null;
        
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            
            while(rs.next()){
                Object[] dados = {rs.getString(1), rs.getString(2), rs.getString(3),
                rs.getString(4), rs.getString(5)};
                dtmHorarios.addRow(dados);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
                    
        }
    }


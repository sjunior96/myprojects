    public void cadastraLinha(){
        String sqlCadastraLinha = "INSERT INTO LINHAS (prefixoLinha, itinerarioLinha) VALUES(?,?)";
        PreparedStatement pstCadastraLinha = null;
        
        try {
            pstCadastraLinha = conexao.prepareStatement(sqlCadastraLinha);
            pstCadastraLinha.setString(1, txtPrefixo.getText().toUpperCase());
            pstCadastraLinha.setString(2, txtItinerario.getText().toUpperCase());
            pstCadastraLinha.executeUpdate();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

